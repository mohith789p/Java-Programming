// Example of Escape Sequences
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello, World!"); // Newline by default
        System.out.println("Line1\nLine2"); // \n for newline
        System.out.println("Tab\tSpace"); // \t for tab
        System.out.println("Quote: \"Java\""); // \" for double quotes
        System.out.println("Backslash: \\"); // \\ for backslash
        System.out.println("Carriage Return:\rOverwritten Text"); // \r for carriage return
        System.out.println("Form Feed: Hello\fWorld"); // \f for form feed
        System.out.println("Backspace: A\bB"); // \b for backspace
        System.out.println("Single Quote: \'Java\'"); // \' for single quote
    }
}
