public class LogicalOperators {
    public static void main(String[] args) {
        boolean x = true, y = false;

        // Logical AND: Returns true only if both x and y are true
        System.out.println("Logical AND (x && y): " + (x && y));

        // Logical OR: Returns true if either x or y is true
        System.out.println("Logical OR (x || y): " + (x || y));

        // Logical NOT: Inverts the value of x
        System.out.println("Logical NOT (!x): " + (!x));

        // Logical NOT: Inverts the value of y
        System.out.println("Logical NOT (!y): " + (!y));
    }
}
